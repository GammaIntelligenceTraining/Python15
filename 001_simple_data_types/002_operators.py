# Python operators
a = 500
b = 200

# Addition '+'
print(a + b)
print(2 + 2)
print(a + 100)

# Subtraction '-'
print(a - b)
print(3 - 1)
print(a - 100)

# Multiplication '*'
print(a * b)
print(2 * 2)
print(a * 2)

# Division '/'
print(a / b)
print(type(a / b))  # Division with '/' will return float, even if integers are divided
print(8 / 2)
print(a / 250)

# Floor division '//'
print(a // b)
print(5 // 2)
print(a // 150)

# Exponentiation '**'
print(a ** b)
print(5 ** 2)
print(a ** 2)

# Modulus or division remainder '%'
print(a % b)
print(5 % 2)
print(a % 150)
