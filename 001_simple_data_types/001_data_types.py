# Python simple data types
# If you want to comment our a line in Python just enter '#' in beginning of line
# print(500)

# In order to print something into a console print() method is used
print(500)

# type() method is used to return data type of an object
print(type(500))

# help() method is used to get more information on object
print(help(500))


# Variables
# 'variable_name' = 'variable_value'
a = 500
print(a)

# Simple data types
# Integer - full number
a = 500
print(a)
print(type(a))

# Float - decimal point number
a = 50.0
print(a)
print(type(a))

# String - text
a = 'Hello world'
print(a)
print(type(a))

# Boolean - True or False (1 or 0)
a = True
print(a)
print(type(a))

# Rounding of numbers
print(round(505.95))
print(round(1.5))
print(round(2.5))
