def squares(x):
    y = x ** 2
    y_string = 'Square of ' + str(x) + ' is ' + str(y) + '.'
    return y_string

def double(x):
    y = x * 2
    y_string = 'Double of ' + str(x) + ' is ' + str(y) + '.'
    return y_string

def tripple(x):
    y = x * 3
    y_string = 'Tripple of ' + str(x) + ' is ' + str(y) + '.'
    return y_string